describe('cross-file consistency', () => {
  let out;
  let models, msgEnums, client, ser;

  before(() => {
    out = generate('enums-and-refs.asyncapi.yaml', { server: 'local' });
    models = readGenerated(out, 'Sources/Models.swift');
    msgEnums = readGenerated(out, 'Sources/MessageEnums.swift');
    client = readGenerated(out, 'Sources/WebSocketClient.swift');
    ser = readGenerated(out, 'Sources/MessageSerializer.swift');
  });

  it('every struct referenced in MessageEnums exists in Models', () => {
    // Extract associated value types from enum cases, excluding String (unknown case)
    const caseTypes = [...msgEnums.matchAll(/case \w+\((\w+)\)/g)]
      .map(m => m[1])
      .filter(t => t !== 'String' && t !== 'Int');
    for (const type of caseTypes) {
      assert.ok(
        models.includes(`public struct ${type}:`),
        `MessageEnums references ${type} but it's not defined in Models.swift`
      );
    }
  });

  it('serializer protocol in client matches serializer file', () => {
    // Extract protocol name from serializer file
    const protoMatch = ser.match(/public protocol (\w+): Sendable/);
    assert.ok(protoMatch, 'no serializer protocol found');
    const protoName = protoMatch[1];
    assert.ok(client.includes(protoName), `client should reference ${protoName}`);
  });

  it('IncomingMessage name in client matches MessageEnums', () => {
    const inMatch = msgEnums.match(/public enum (\w*IncomingMessage):/);
    assert.ok(inMatch, 'no IncomingMessage enum found');
    assert.ok(client.includes(inMatch[1]), `client should reference ${inMatch[1]}`);
  });

  it('all generated Swift files have the generation header', () => {
    const files = listGeneratedSwiftFiles(out);
    for (const file of files) {
      const content = readGenerated(out, `Sources/${file}`);
      assert.ok(
        content.includes('// Generated by asyncapi-swift-ws-template'),
        `${file} missing generation header`
      );
    }
  });

  it('all generated Swift files import Foundation', () => {
    const files = listGeneratedSwiftFiles(out);
    for (const file of files) {
      const content = readGenerated(out, `Sources/${file}`);
      assert.ok(content.includes('import Foundation'), `${file} missing Foundation import`);
    }
  });
});
