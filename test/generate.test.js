describe('enums-and-refs spec', () => {
  let out;

  before(() => {
    out = generate('enums-and-refs.asyncapi.yaml', { server: 'local' });
  });

  it('generates Enums.swift', () => {
    assert.ok(generatedFileExists(out, 'Sources/Enums.swift'));
  });

  describe('Enums.swift', () => {
    let enums;
    before(() => { enums = readGenerated(out, 'Sources/Enums.swift'); });

    it('generates Priority enum', () => {
      assert.ok(enums.includes('public enum Priority: String, Codable, Sendable'));
    });

    it('Priority has all expected cases', () => {
      assert.ok(enums.includes('case low'));
      assert.ok(enums.includes('case medium'));
      assert.ok(enums.includes('case high'));
      assert.ok(enums.includes('case critical'));
    });

    it('generates a status enum (deduped)', () => {
      // The status and new_status properties share the same values.
      // Only one enum should exist for this set of values.
      const enumCount = (enums.match(/public enum \w+: String, Codable, Sendable/g) || []).length;
      assert.equal(enumCount, 2, 'should have exactly 2 enums (priority + status)');
    });

    it('status enum has correct cases', () => {
      assert.ok(enums.includes('case pending'));
      assert.ok(enums.includes('case active'));
      assert.ok(enums.includes('case archived'));
    });
  });

  describe('Models.swift — $ref resolution', () => {
    let models;
    before(() => { models = readGenerated(out, 'Sources/Models.swift'); });

    it('generates ItemData struct from component schema', () => {
      assert.ok(models.includes('public struct ItemData: Codable, Sendable'));
    });

    it('resolves $ref in ItemCreated to ItemData type', () => {
      assert.ok(models.includes('public let item: ItemData'));
    });

    it('resolves array $ref in ItemList to [ItemData]', () => {
      assert.ok(models.includes('public let items: [ItemData]'));
    });

    it('renames Error schema to ServerError', () => {
      assert.ok(models.includes('public struct ServerError: Codable, Sendable'));
      assert.ok(!models.includes('public struct Error:'));
    });
  });

  describe('Models.swift — property types', () => {
    let models;
    before(() => { models = readGenerated(out, 'Sources/Models.swift'); });

    it('uses enum types for enum properties', () => {
      assert.ok(models.includes(': Priority'));
    });

    it('handles CodingKeys for snake_case fields', () => {
      assert.ok(models.includes('case itemId = "item_id"'));
    });

    it('makes non-required properties optional', () => {
      // item in ItemCreated is not required
      assert.ok(models.includes('public let item: ItemData?'));
    });
  });
});

