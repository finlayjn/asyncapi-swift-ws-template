// test/generate.test.js — End-to-end tests for Swift output generation
//
// Run with: node --test test/generate.test.js
//
// These tests generate Swift code from fixture AsyncAPI specs and assert
// on file structure, naming conventions, type correctness, and parameter behavior.

const { describe, it, before, after } = require('node:test');
const assert = require('node:assert/strict');
const { generate, readGenerated, generatedFileExists, listGeneratedSwiftFiles, cleanOutput } = require('./helpers');

// ─────────────────────────────────────────────────────────────────────────────
// Basic spec — minimal send/receive with no enums, no $ref, no msgpack
// ─────────────────────────────────────────────────────────────────────────────

describe('basic spec (JSON, reconnect, no prefix)', () => {
  let out;

  before(() => {
    out = generate('basic.asyncapi.yaml', { server: 'local' });
  });

  // ── File structure ──

  it('generates Package.swift', () => {
    assert.ok(generatedFileExists(out, 'Package.swift'));
  });

  it('generates all expected Swift source files', () => {
    const files = listGeneratedSwiftFiles(out);
    assert.ok(files.includes('Models.swift'), 'missing Models.swift');
    assert.ok(files.includes('MessageEnums.swift'), 'missing MessageEnums.swift');
    assert.ok(files.includes('MessageSerializer.swift'), 'missing MessageSerializer.swift');
    assert.ok(files.includes('WebSocketClient.swift'), 'missing WebSocketClient.swift');
  });

  it('does not generate Enums.swift when spec has no enums', () => {
    assert.ok(!generatedFileExists(out, 'Sources/Enums.swift'));
  });

  // ── Package.swift ──

  describe('Package.swift', () => {
    let pkg;
    before(() => { pkg = readGenerated(out, 'Package.swift'); });

    it('uses swift-tools-version 6.0', () => {
      assert.ok(pkg.startsWith('// swift-tools-version: 6.0'));
    });

    it('imports PackageDescription', () => {
      assert.ok(pkg.includes('import PackageDescription'));
    });

    it('derives package name from spec title', () => {
      assert.ok(pkg.includes('name: "TestBasicWS"'));
    });

    it('does not include msgpack dependency', () => {
      assert.ok(!pkg.includes('msgpack-swift'));
      assert.ok(!pkg.includes('DMMessagePack'));
    });

    it('targets Apple platforms', () => {
      assert.ok(pkg.includes('.iOS('));
      assert.ok(pkg.includes('.macOS('));
    });
  });

  // ── Models.swift ──

  describe('Models.swift', () => {
    let models;
    before(() => { models = readGenerated(out, 'Sources/Models.swift'); });

    it('has generation header', () => {
      assert.ok(models.includes('// Generated by asyncapi-swift-ws-template'));
    });

    it('imports Foundation', () => {
      assert.ok(models.includes('import Foundation'));
    });

    it('generates Codable & Sendable structs for send messages', () => {
      assert.ok(models.includes('public struct Ping: Codable, Sendable'));
      assert.ok(models.includes('public struct Echo: Codable, Sendable'));
    });

    it('generates Codable & Sendable structs for receive messages', () => {
      assert.ok(models.includes('public struct Pong: Codable, Sendable'));
      assert.ok(models.includes('public struct EchoReply: Codable, Sendable'));
      assert.ok(models.includes('public struct ServerError: Codable, Sendable'));
    });

    it('uses public let for properties', () => {
      assert.ok(models.includes('public let message: String'));
    });

    it('generates CodingKeys for snake_case properties', () => {
      assert.ok(models.includes('case serverTime = "server_time"'));
    });

    it('handles Error → ServerError reserved type rename', () => {
      assert.ok(models.includes('public struct ServerError: Codable, Sendable'));
      assert.ok(!models.includes('public struct Error:'));
    });
  });

  // ── MessageEnums.swift ──

  describe('MessageEnums.swift', () => {
    let enums;
    before(() => { enums = readGenerated(out, 'Sources/MessageEnums.swift'); });

    it('defines OutgoingMessage enum', () => {
      assert.ok(enums.includes('public enum OutgoingMessage: Sendable'));
    });

    it('defines IncomingMessage enum', () => {
      assert.ok(enums.includes('public enum IncomingMessage: Sendable'));
    });

    it('has outgoing cases for send messages', () => {
      assert.ok(enums.includes('case ping(Ping)'));
      assert.ok(enums.includes('case echo(Echo)'));
    });

    it('has incoming cases for receive messages', () => {
      assert.ok(enums.includes('case pong(Pong)'));
      assert.ok(enums.includes('case echoReply(EchoReply)'));
    });

    it('has unknown case for unrecognized incoming messages', () => {
      assert.ok(enums.includes('case unknown(String)'));
    });

    it('makes OutgoingMessage Encodable', () => {
      assert.ok(enums.includes('extension OutgoingMessage: Encodable'));
    });

    it('makes IncomingMessage Decodable', () => {
      assert.ok(enums.includes('extension IncomingMessage: Decodable'));
    });

    it('decodes on the type discriminator field', () => {
      assert.ok(enums.includes('case type'));
      assert.ok(enums.includes('TypeKey'));
    });
  });

  // ── MessageSerializer.swift ──

  describe('MessageSerializer.swift', () => {
    let ser;
    before(() => { ser = readGenerated(out, 'Sources/MessageSerializer.swift'); });

    it('defines MessageSerializer protocol', () => {
      assert.ok(ser.includes('public protocol MessageSerializer: Sendable'));
    });

    it('defines JSONMessageSerializer', () => {
      assert.ok(ser.includes('public struct JSONMessageSerializer: MessageSerializer'));
    });

    it('does not generate MessagePackSerializer', () => {
      assert.ok(!ser.includes('MessagePackSerializer'));
      assert.ok(!ser.includes('import MessagePack'));
    });
  });

  // ── WebSocketClient.swift ──

  describe('WebSocketClient.swift', () => {
    let client;
    before(() => { client = readGenerated(out, 'Sources/WebSocketClient.swift'); });

    it('defines ConnectionState enum', () => {
      assert.ok(client.includes('public enum ConnectionState: Sendable, Equatable'));
    });

    it('includes reconnecting state by default', () => {
      assert.ok(client.includes('case reconnecting(attempt: Int)'));
    });

    it('defines ClientConfiguration struct', () => {
      assert.ok(client.includes('public struct ClientConfiguration: Sendable'));
    });

    it('defines WebSocketClient actor', () => {
      assert.ok(client.includes('public actor WebSocketClient'));
    });

    it('defines error enum using Swift.Error', () => {
      assert.ok(client.includes('public enum WebSocketClientError: Swift.Error, Sendable'));
    });

    it('includes default URL from server config', () => {
      assert.ok(client.includes('ws://localhost:9000'));
    });

    it('has auto-reconnect config fields', () => {
      assert.ok(client.includes('autoReconnect'));
      assert.ok(client.includes('maxReconnectAttempts'));
      assert.ok(client.includes('baseReconnectDelay'));
    });

    it('uses AsyncStream for messages', () => {
      assert.ok(client.includes('AsyncStream<IncomingMessage>'));
    });

    it('uses AsyncStream for state', () => {
      assert.ok(client.includes('AsyncStream<ConnectionState>'));
    });
  });
});

