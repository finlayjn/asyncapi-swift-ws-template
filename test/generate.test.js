describe('typePrefix=TST', () => {
  let out;

  before(() => {
    out = generate('enums-and-refs.asyncapi.yaml', { server: 'local', typePrefix: 'TST' });
  });

  describe('Models.swift', () => {
    let models;
    before(() => { models = readGenerated(out, 'Sources/Models.swift'); });

    it('prefixes all struct names', () => {
      assert.ok(models.includes('public struct TSTCreateItem:'));
      assert.ok(models.includes('public struct TSTItemCreated:'));
      assert.ok(models.includes('public struct TSTItemData:'));
      assert.ok(models.includes('public struct TSTServerError:'));
    });

    it('does not contain unprefixed struct names', () => {
      assert.ok(!models.match(/public struct (?!TST)\w+:/));
    });

    it('prefixes $ref type references', () => {
      assert.ok(models.includes(': TSTItemData'));
    });
  });

  describe('Enums.swift', () => {
    let enums;
    before(() => { enums = readGenerated(out, 'Sources/Enums.swift'); });

    it('prefixes enum names', () => {
      assert.ok(enums.includes('public enum TSTPriority:'));
    });

    it('does not double-prefix', () => {
      assert.ok(!enums.includes('TSTTSTT'));
    });
  });

  describe('MessageEnums.swift', () => {
    let msgEnums;
    before(() => { msgEnums = readGenerated(out, 'Sources/MessageEnums.swift'); });

    it('prefixes OutgoingMessage and IncomingMessage', () => {
      assert.ok(msgEnums.includes('public enum TSTOutgoingMessage:'));
      assert.ok(msgEnums.includes('public enum TSTIncomingMessage:'));
    });

    it('uses prefixed struct types in cases', () => {
      assert.ok(msgEnums.includes('case createItem(TSTCreateItem)'));
    });
  });

  describe('MessageSerializer.swift', () => {
    let ser;
    before(() => { ser = readGenerated(out, 'Sources/MessageSerializer.swift'); });

    it('prefixes protocol and struct names', () => {
      assert.ok(ser.includes('public protocol TSTMessageSerializer:'));
      assert.ok(ser.includes('public struct TSTJSONMessageSerializer: TSTMessageSerializer'));
    });
  });

  describe('WebSocketClient.swift', () => {
    let client;
    before(() => { client = readGenerated(out, 'Sources/WebSocketClient.swift'); });

    it('prefixes all top-level types', () => {
      assert.ok(client.includes('public enum TSTConnectionState:'));
      assert.ok(client.includes('public struct TSTClientConfiguration:'));
      assert.ok(client.includes('public actor TSTWebSocketClient'));
      assert.ok(client.includes('public enum TSTWebSocketClientError:'));
    });

    it('references prefixed serializer types', () => {
      assert.ok(client.includes('TSTMessageSerializer'));
      assert.ok(client.includes('TSTJSONMessageSerializer'));
    });

    it('references prefixed message types', () => {
      assert.ok(client.includes('TSTIncomingMessage'));
      assert.ok(client.includes('TSTOutgoingMessage'));
    });
  });
});

